
public class TypeCastingDemo {

	public static void main(String[] args) {
		
		
			short s1 = 10;
			int x  =s1;
			System.out.println(x);
			
			
			int x1 = 300;
			double d = x1;
			System.out.println(d);
			
			double d1 = 10.5;
			//explicit casting
			int x2 = (int) d1;
			System.out.println(x2);
			
			
			
			
		

	}

}
